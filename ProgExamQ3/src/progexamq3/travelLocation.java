    //Method to check if start and end Location are the same

    private boolean checkStartEnd() {
        //If destinations are the same value will change to true
        boolean valid = false;
        //Check if destinations are the same
        if (startLocation.getSelectedItem().equals(endLocation.getSelectedItem())) {
            valid = true;
            //Inform user that destinations cannot be the same
            JOptionPane.showMessageDialog(null, "Destinations cannot be the same",
                    "Message", JOptionPane.INFORMATION_MESSAGE);
        }
        return valid;
    }

