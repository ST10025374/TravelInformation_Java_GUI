    //Button SUBMIT
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //CheckStartEnd Method will return either true or false
        boolean valid = false;
        //Store info to be saved
        String travelInfo = "TRAVEL LOG"
                + "\n*****************************"
                + "\nDestination 1: " + startLocation.getSelectedItem().toString()
                + "\nDestination 2: " + endLocation.getSelectedItem().toString()
                + "\nTravel By: " + travelBy.getSelectedItem().toString()
                + "\n*****************************";
        //Calling method to check if start and end are equal
        valid = checkStartEnd();
        //if valid is stop impossibilitate data submission  
        if (valid == true) {
            return;
        }
        //Write to file
        try {
            File file = new File("travel.txt");//Created a File object called file
            FileWriter fw = new FileWriter(file, true);//created a fileWriter object called fw
            PrintWriter pw = new PrintWriter(fw);//created a PrintWriter object called pw

            pw.println(travelInfo);//Pass String to Print in text file 
            pw.close();
        } catch (IOException ioe) {
            //Inform user that something happened
            JOptionPane.showMessageDialog(null, "An error happened",
                    "Message", JOptionPane.ERROR_MESSAGE);
        }
        //Add data to be displayed 
        try {
            BufferedReader reader = new BufferedReader(new FileReader("travel.txt"));
            String line = null;
            //Continue reading text lines till there none
            while ((line = reader.readLine()) != null) {
                display.append(line + "\n");//Pass lines to be displayed
                System.out.println(line);
            }
        } catch (IOException e) {
            //Inform user that something happened
            JOptionPane.showMessageDialog(null, "An error happened",
                    "Message", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
